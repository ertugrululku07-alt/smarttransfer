#!/usr/bin/env node
// ══════════════════════════════════════════════════════════════
//  SmartTransfer — Automatic Setup Script
//  Usage: node setup.js
//
//  This script reads setup.config.json and automatically:
//  1. Generates all .env files (backend + frontend)
//  2. Updates mobile app config files (driver-app + partner-app)
//  3. Installs npm dependencies
//  4. Runs Prisma migrations
//  5. Builds frontend
//  6. Generates PM2 ecosystem file
//  7. Generates Nginx config
//  8. Optionally sets up SSL with certbot
// ══════════════════════════════════════════════════════════════

const fs = require('fs');
const path = require('path');
const { execSync, spawn } = require('child_process');
const crypto = require('crypto');
const readline = require('readline');

const ROOT = __dirname;
const CONFIG_FILE = path.join(ROOT, 'setup.config.json');

// ── Colors for terminal output ──
const c = {
  reset: '\x1b[0m',
  bright: '\x1b[1m',
  green: '\x1b[32m',
  yellow: '\x1b[33m',
  red: '\x1b[31m',
  cyan: '\x1b[36m',
  blue: '\x1b[34m',
  magenta: '\x1b[35m',
  dim: '\x1b[2m',
};

function log(msg) { console.log(`${c.green}✓${c.reset} ${msg}`); }
function warn(msg) { console.log(`${c.yellow}⚠${c.reset} ${msg}`); }
function err(msg) { console.log(`${c.red}✗${c.reset} ${msg}`); }
function info(msg) { console.log(`${c.cyan}ℹ${c.reset} ${msg}`); }
function header(msg) { console.log(`\n${c.bright}${c.blue}═══ ${msg} ═══${c.reset}\n`); }

function ask(question) {
  const rl = readline.createInterface({ input: process.stdin, output: process.stdout });
  return new Promise(resolve => rl.question(`${c.cyan}?${c.reset} ${question} `, ans => { rl.close(); resolve(ans.trim()); }));
}

// ── Load or create config ──
function loadConfig() {
  if (!fs.existsSync(CONFIG_FILE)) {
    err('setup.config.json bulunamadı!');
    info('Lütfen önce setup.config.json dosyasını oluşturun.');
    process.exit(1);
  }

  const raw = fs.readFileSync(CONFIG_FILE, 'utf8');
  // Strip comment keys (keys starting with "//")
  const config = JSON.parse(raw);
  for (const key of Object.keys(config)) {
    if (key.startsWith('//')) delete config[key];
  }
  return config;
}

// ── Generate random JWT secret if empty ──
function ensureJwtSecret(config) {
  if (!config.security.jwtSecret) {
    config.security.jwtSecret = crypto.randomBytes(32).toString('hex');
    info(`JWT Secret otomatik oluşturuldu: ${config.security.jwtSecret.substring(0, 16)}...`);
  }
}

// ══════════════════════════════════════════════════════════════
//  STEP 1: Generate Backend .env
// ══════════════════════════════════════════════════════════════
function generateBackendEnv(config) {
  const protocol = config.ssl?.enabled ? 'https' : 'http';
  const backendUrl = `${protocol}://${config.domain.backend}`;
  const frontendUrl = `${protocol}://${config.domain.frontend}`;

  const env = `# ══════════════════════════════════════════════════════════════
# SmartTransfer Backend — Auto-generated by setup.js
# Generated: ${new Date().toISOString()}
# ══════════════════════════════════════════════════════════════

# PostgreSQL
DATABASE_URL="${config.database.url}"

# JWT
JWT_SECRET=${config.security.jwtSecret}
JWT_EXPIRATION=${config.security.jwtExpiration || '7d'}
REFRESH_TOKEN_EXPIRATION=${config.security.refreshTokenExpiration || '7d'}

# Server
PORT=${config.server.backendPort || 4000}
NODE_ENV=${config.server.nodeEnv || 'production'}

# Public URLs
BACKEND_URL=${backendUrl}
FRONTEND_URL=${frontendUrl}

# API Keys
AVIATIONSTACK_API_KEY=${config.apiKeys?.aviationstackApiKey || ''}

# Integrations
N8N_WEBHOOK_URL=${config.integrations?.n8nWebhookUrl || ''}
N8N_WEBHOOK_SECRET=${config.security?.n8nWebhookSecret || crypto.randomBytes(24).toString('hex')}

# Security tokens (auto-generated if missing)
AI_SECRET_TOKEN=${config.security?.aiSecretToken || crypto.randomBytes(32).toString('hex')}
UETDS_ENCRYPTION_KEY=${config.security?.uetdsEncryptionKey || crypto.randomBytes(16).toString('hex') + crypto.randomBytes(16).toString('hex')}

# Tenant & CORS
DEFAULT_TENANT_SLUG=${config.company.tenantSlug || 'smarttravel-demo'}
CORS_ORIGINS=${frontendUrl}
`;

  const envPath = path.join(ROOT, 'backend', '.env');
  fs.writeFileSync(envPath, env, 'utf8');
  log(`Backend .env oluşturuldu: ${envPath}`);

  // Also create .env.production
  fs.writeFileSync(path.join(ROOT, 'backend', '.env.production'), env, 'utf8');
}

