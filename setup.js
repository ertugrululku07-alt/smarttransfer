  const errors = [];
  if (!config.domain?.frontend) errors.push('domain.frontend boş');
  if (!config.domain?.backend) errors.push('domain.backend boş');
  if (!config.database?.url || config.database.url.includes('user:password')) errors.push('database.url ayarlanmamış');

